  static Locale resolve(Locale? locale, Iterable<Locale> _) =>
      locale?.languageCode.toLowerCase() == 'ru'
          ? const Locale('ru', 'RU')
          : const Locale('zh', 'CN');

  static String translate(BuildContext context, String source) =>
      translateForLocale(source, Localizations.localeOf(context));

  static String translateForLocale(String source, Locale locale) {
    if (locale.languageCode != 'ru' || source.isEmpty) return source;
    final exact = _ru[source];
    if (exact != null) return exact;

    final hello = RegExp(r'^\u4f60\u597d\uff0c(.+)$').firstMatch(source);
    if (hello != null) return 'Здравствуйте, ${hello.group(1)}';

    final meetingInvitation = RegExp(r'^收到会议邀请：(.+)$').firstMatch(source);
    if (meetingInvitation != null) {
      return 'Новое приглашение: ${meetingInvitation.group(1)}';
    }

    final directChat = RegExp(r'^与 (.+) 一对一私聊$').firstMatch(source);
    if (directChat != null) {
      return 'Личный чат с ${directChat.group(1)}';
    }

    final unreadDirect = RegExp(r'^(\d+) 条新私聊消息$').firstMatch(source);
    if (unreadDirect != null) {
      return '${unreadDirect.group(1)} новых сообщений';
    }

    final directRecords = RegExp(r'^(\d+) 条翻译记录$').firstMatch(source);
    if (directRecords != null) {
      return '${directRecords.group(1)} реплик с переводом';
    }

    final minutes = RegExp(r'^(\d+) \u5206\u949f$').firstMatch(source);
    if (minutes != null) return '${minutes.group(1)} \u043c\u0438\u043d';

    final participantCount =
        RegExp(r'^\u53c2\u4e0e\uff1a(\d+) \u4eba$').firstMatch(source);
    if (participantCount != null) {
      return 'Участников: ${participantCount.group(1)}';
    }

    final sendRecipients = RegExp(r'^发送给 (\d+) 人$').firstMatch(source);
    if (sendRecipients != null) {
      return 'Отправить: ${sendRecipients.group(1)}';
    }

    final emailResult = RegExp(r'^成功 (\d+) 人，失败 (\d+) 人$').firstMatch(source);
    if (emailResult != null) {
      return 'Успешно: ${emailResult.group(1)}, ошибок: ${emailResult.group(2)}';
    }

    final status = RegExp(r'^\u72b6\u6001\uff1a(.+)$').firstMatch(source);
    if (status != null) {
      return 'Статус: ${_ru[status.group(1)] ?? status.group(1)}';
    }

    final historyPolicy =
        RegExp(r'^\u4f1a\u540e\u6743\u9650\uff1a(.+)$').firstMatch(source);
    if (historyPolicy != null) {
      return 'Доступ после встречи: '
          '${_ru[historyPolicy.group(1)] ?? historyPolicy.group(1)}';
    }

    final messageCount =
        RegExp(r'^(\d+) \u6761\u6d88\u606f \u00b7 (.+) \u00b7 (.+)$')
            .firstMatch(source);
    if (messageCount != null) {
      return '${messageCount.group(1)} \u0441ообщ. · '
          '${translateForLocale(messageCount.group(2)!, locale)} · '
          '${_ru[messageCount.group(3)] ?? messageCount.group(3)}';
    }

    final currentDevice = RegExp(r'^(.+)\uff08\u5f53\u524d\u8bbe\u5907\uff09$')
        .firstMatch(source);
    if (currentDevice != null) return '${currentDevice.group(1)} (текущее)';

    final revokedDevice =
        RegExp(r'^(.+)\uff08\u5df2\u4e0b\u7ebf\uff09$').firstMatch(source);
    if (revokedDevice != null) return '${revokedDevice.group(1)} (отключено)';

    final removed = RegExp(
            r'^\u5df2\u79fb\u51fa (.+)\uff0c\u65e7\u9080\u8bf7\u5df2\u5931\u6548$')
        .firstMatch(source);
    if (removed != null) {
      return '${removed.group(1)} удалён; старое приглашение недействительно';
    }

    final speak = RegExp(r'^\u6309\u4f4f\u8bf4(.+)$').firstMatch(source);
    if (speak != null) {
      return 'Удерживайте, чтобы говорить '
          '${_ru[speak.group(1)] ?? speak.group(1)}';
    }

    final languageSource =
        RegExp(r'^(\u4e2d\u6587|\u4fc4\u8bed)\u539f\u6587$').firstMatch(source);
    if (languageSource != null) {
      return 'Оригинал (${_ru[languageSource.group(1)]})';
    }
    final languageTarget =
        RegExp(r'^(\u4e2d\u6587|\u4fc4\u8bed)\u8bd1\u6587$').firstMatch(source);
    if (languageTarget != null) {
      return 'Перевод (${_ru[languageTarget.group(1)]})';
    }

    var translated = source;
    for (final entry in _ruFragments.entries) {
      translated = translated.replaceAll(entry.key, entry.value);
    }
    return translated;
  }

