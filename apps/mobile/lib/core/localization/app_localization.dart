import 'package:flutter/material.dart';

enum AppLanguageMode {
  system,
  zh,
  ru;

  static AppLanguageMode parse(String? value) => switch (value) {
        'zh' => AppLanguageMode.zh,
        'ru' => AppLanguageMode.ru,
        _ => AppLanguageMode.system,
      };

  Locale? get locale => switch (this) {
        AppLanguageMode.system => null,
        AppLanguageMode.zh => const Locale('zh', 'CN'),
        AppLanguageMode.ru => const Locale('ru', 'RU'),
      };
}

